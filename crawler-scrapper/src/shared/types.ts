// types.ts
export interface VolumeSimple {
    numero: number;
    image: string | undefined;
}

export interface VolumeSpecial {
    titre: string;
    image: string | undefined;
}

export interface VolumeCollector {
    titre: string;
    image: string | undefined;
}

export interface VolumesData {
    simple: VolumeSimple[];
    special: VolumeSpecial[];
    collector: VolumeCollector[];
}

export interface MangaData {
    url: string | undefined;
    titre: string;
    titreOriginal: string;
    origine: string;
    anneeVF: string;
    type: string;
    genres: string[];
    themes: string[];
    auteur: string;
    traducteur: string;
    editeurVO: string;
    editeurVF: string;
    nbVolumesVO: string;
    nbVolumesVF: string;
    prix: string;
    volumes: VolumesData;
    statut: string;
}
                                                      
